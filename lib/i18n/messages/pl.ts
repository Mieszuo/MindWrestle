// `pl` is declared `as const` so nested values (including function-valued
// entries added by later tasks) keep their precise inferred types. That same
// literal-narrowing would otherwise force `en` to use the exact Polish
// string values, so `Messages` widens string literals to `string` while
// leaving the object shape (and any function signatures) intact — the
// parity test then enforces structural (key-path) equality between catalogs.
type Widen<T> = T extends (...args: never[]) => unknown
  ? T
  : T extends string
    ? string
    : T extends readonly (infer U)[]
      ? readonly Widen<U>[]
      : T extends object
        ? { [K in keyof T]: Widen<T[K]> }
        : T;

export type Messages = Widen<typeof pl>;
